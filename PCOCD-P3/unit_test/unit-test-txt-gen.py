nums = [
    0xc42, # 0x40
    0xc40, # 0x41
    0xc45, # 0x42
    0xc43, # 0x43
    0xc40, # 0x44
    0xc43, # 0x45
    0x0003, # 0x46
    0xffff, # 0x47
    0xfffe, # 0x48
    0x0003, # 0x49
    0x0000, # 0x4a
    0xfffb, # 0x4b
]

with open('unit-test.txt','w') as f:
    for i in range(0x100):
        f.write('{:02X}\n'.format(i))
    for n in nums:
        for i in range(4):
            f.write('{:02X}\n'.format(int(n/0x100_00_00%0x100)))
            n *= 0x100
